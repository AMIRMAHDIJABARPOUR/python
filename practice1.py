# محاسبه مجموع اعداد زوج
status = False
while not status:
    number = int(input("enter your Number: "))
    if number < 1:
        print("enter a larger number")
    else:
        total = 0
        for num in range(1, number + 1):
            if num % 2 == 0:
                total += num
        print(f"Sum of even numbers: {total}")
        status = True


